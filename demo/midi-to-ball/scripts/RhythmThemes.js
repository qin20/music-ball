/**
 * RhythmThemes.js（节奏系统主题配置模块）
 * =======================================
 *
 * 📦 模块简介
 * ---------------------------------------
 * RhythmThemes 模块定义了节奏动画系统的可视化主题样式。
 * 每个主题包含小球绘制函数、拖尾效果、墙体尺寸与颜色、背景色等配置。
 *
 * 该模块允许通过传入不同的 `themeName`，动态切换视觉风格，适配不同情绪、音乐类型或交互需求。
 * 默认主题 `default` 为白色发光小球 + 深灰墙体 + 中灰背景，具备高对比、柔和亮度感。
 *
 *
 * 🧰 主题配置项说明（以每个主题为单位）
 * ---------------------------------------
 *
 * | 属性名         | 类型     | 说明                                                             |
 * |----------------|----------|------------------------------------------------------------------|
 * | `ballStyle`    | Function | 绘制小球样式，参数：ctx, x, y, radius                            |
 * | `drawTrail`    | Function | 绘制拖尾粒子样式，参数：ctx, particle, radius                    |
 * | `characterSize`   | Number   | 小球半径（像素）                                                 |
 * | `wallColor`    | String   | 墙体的基础颜色（支持任意 CSS 色值）                             |
 * | `glowColor`    | String   | 小球撞击墙体时墙体的发光颜色                                     |
 * | `background`   | String   | Canvas 背景颜色                                                  |
 *
 *
 * 📦 功能结构说明
 * ---------------------------------------
 *
 * - `RhythmThemes.default`
 *   默认主题配置对象。包含白色发光球、亮白拖尾、深灰墙体、中灰背景。
 *   可根据此结构扩展自定义主题，例如：`nightSky`, `candleGlow`, `mistBlue` 等。
 *
 * - 每个主题中的 `ballStyle()` 和 `drawTrail()` 可自由定制渐变、描边、阴影等视觉特效。
 *
 *
 * 🧪 开发过程摘要 / 关键对话摘录
 * ---------------------------------------
 *
 * - ✅ 用户希望将视觉风格与核心逻辑解耦，形成可插拔式的主题系统。
 * - ✅ 初期尝试为小球实现纯色 + 光晕拖尾，后来加入 RadialGradient 提升质感。
 * - ✅ 为保持视觉统一性，将墙体颜色、厚度、发光色等也归入主题集中管理。
 * - ✅ 用户明确提出希望默认主题以“灰色背景 + 白色球 + 偏黑墙体”为基调。
 * - ✅ 模块已成功应用于节奏系统 Rhythm.js / RhythmBall.js 等可视化组件中。
 */


